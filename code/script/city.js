class City{
    #name;
    #country;
    #description;
    #humidity;

    get name(){

        return this.#name;
    }

    set name(value){
        this.#name = value;
    }
    get country(){

        return this.#country;
    }

    set country(value){
        this.#country = value;
    }

    get description(){

        return this.#description;
    }

    set description(value){
        this.#description = value;
    }

    get humidity(){

        return this.#humidity;
    }

    set humidity(value){
        this.#humidity = value;
    }


} 